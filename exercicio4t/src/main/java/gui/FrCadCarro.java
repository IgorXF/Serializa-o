    public void habilitarCampos(boolean flag) {
        edtId.setEnabled(flag);
        edtAno.setEnabled(flag);
        edtMarca.setEnabled(flag);
        edtPreco.setEnabled(flag);
        edtModelo.setEnabled(flag);
    }

    public void limparCampos() {
        edtId.setText("");
        edtAno.setText("");
        edtMarca.setText("");
        edtPreco.setText("");
        edtModelo.setText("");
    }

    public void objetoParaCampos(Carro a) {
        edtId.setText(a.getId());
        edtMarca.setText(a.getMarca());
        edtModelo.setText(a.getModelo());
        edtAno.setText(a.getAno() + "");
        edtPreco.setText(a.getPreco() + "");
    }

    public Carro camposParaObjeto() {
        Carro a = new Carro();
        a.setId(edtId.getText());
        a.setMarca(edtMarca.getText());
        a.setModelo(edtModelo.getText());
        
        String ano = edtAno.getText();
        int c = 0;
        if (!ano.isEmpty()) {
            c = Integer.parseInt(ano);
        }
        a.setAno(c);

        
        String preco = edtPreco.getText();
        double b = 0.0;
        if (!preco.isEmpty()) {
            b = Double.parseDouble(preco);
        }
        a.setPreco(b);
        return a;
    }

