    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idEscolhido = JOptionPane.showInputDialog("Informe o Id do Carro que deseja EDITAR:", "");

        Carro carroEditando = gerenciador.buscarCarro(idEscolhido);

        if (carroEditando == null) {
            JOptionPane.showMessageDialog(this, "N√£o existe este carro.");
            this.editando = false;
        } else {
            this.limparCampos();
            this.habilitarCampos(true);

            this.objetoParaCampos(carroEditando);
            this.editando = true;
            this.idAntigo = carroEditando.getId();
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idEscolhido = JOptionPane.showInputDialog("Informe o Id do carro que deseja excluir:", "");

        Carro a = gerenciador.buscarCarro(idEscolhido);

        if (a == null) {
            JOptionPane.showMessageDialog(this, "N√£o existe este Carro.");
        } else {
            gerenciador.removerCarro(idEscolhido);
            JOptionPane.showMessageDialog(this, "Exclus√£o feita com sucesso!");
        }
        
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);

    }//GEN-LAST:event_btnExcluirActionPerformed

    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        Carro a = this.camposParaObjeto();


        if(this.editando){
            gerenciador.atualizarCarro(idAntigo, a);
        }else{
            gerenciador.adicionarCarro(a);
        }

        
        //No final reset a tela
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;

        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
        
        try {
            gerenciador.salvarNoArquivo("ListagemCarros.csv");
        } catch (IOException ex) {
            Logger.getLogger(FrCadCarro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bntSalvarActionPerformed

